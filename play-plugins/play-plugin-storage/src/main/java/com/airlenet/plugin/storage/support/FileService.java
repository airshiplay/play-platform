package com.airlenet.plugin.storage.support;

import java.io.InputStream;
import java.util.List;

import org.springframework.core.io.Resource;
import org.springframework.web.multipart.MultipartFile;

public interface FileService {

	/**
	 * 文件验证
	 * 
	 * @param fileType
	 *            文件类型
	 * @param multipartFile
	 *            上传文件
	 * @return 文件验证是否通过
	 */
	boolean isValid(MultipartFile multipartFile);

	/**
	 * 文件上传
	 * 
	 * @param fileType
	 *            文件类型
	 * @param multipartFile
	 *            上传文件
	 * @param async
	 *            是否异步
	 * @return 访问URL
	 */
	String upload(MultipartFile multipartFile, boolean async);

	/**
	 * 文件上传(同步)
	 * 
	 * @param fileType
	 *            文件类型
	 * @param multipartFile
	 *            上传文件
	 * @return 访问URL
	 */
	String upload(MultipartFile multipartFile,String dir);

    /**
     *
     * @param multipartFile
     * @return
     */
    String upload(MultipartFile multipartFile);
	/**
	 * 文件上传至本地
	 * 
	 * @param fileType
	 *            文件类型
	 * @param multipartFile
	 *            上传文件
	 * @return 路径
	 */
	String uploadLocal(MultipartFile multipartFile,String dir);
	
	String uploadLocal(MultipartFile multipartFile,String dir, Visitor visitor);

	String uploadLocal(InputStream inputStream,String dir, String filename);
	
	String uploadLocal(InputStream inputStream,String dir, String filename, Visitor visitor);

	/**
	 * 文件浏览
	 * 
	 * @param path
	 *            浏览路径
	 * @param fileType
	 *            文件类型
	 * @param orderType
	 *            排序类型
	 * @return 文件信息
	 */
	List<FileInfo> browser(String path, String orderProperty);

	Resource toResource(String path);

}
